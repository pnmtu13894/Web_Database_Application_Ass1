    {
        //
        $this->validate($request, [
            'ticket_comment'=> 'required|max:255'
        ]);

        $comment = new Comment();

        $comment->ticket_id = $request->ticket_id;
        $comment->content = $request->ticket_comment;

        $comment->save();


//        return $request->all();

        return redirect('/admin');

    }

